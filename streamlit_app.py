import streamlit as st
from utils.save_to_document import save_document
import datetime
import requests
import sys

BASE_URL = "http://localhost:8000" # backend endpoint

st.set_page_config(
    page_title="ðŸŽ† Travel Planning Agentic Application",
    page_icon="ðŸŽ†",
    layout="centered",
    initial_sidebar_state="expanded",
)

st.title("ðŸŽ† Travel Planning Agentic Application")

if "messages" not in st.session_state:
    st.session_state.messages = []

st.header("How can i help you plan a trip ? Let me know where you want to visit.")

with st.form(key="query_form", clear_on_submit=True):
    user_input = st.text_input("User Input", placeholder='e.g. Plan a 5 day trip to TÃ¼rkiye')
    submit_button=st.form_submit_button('Send')

if submit_button and user_input.strip():
    try:
        with st.spinner("Bot is thinking...."):
            payload={"query":user_input}
            response=requests.post(f"{BASE_URL}/query",json=payload)

        if response.status_code==200:
            answer=response.json().get('answer','No answer returned.')      #.json() converts json to python dictionary

            if answer!='No answer returned.':
                save_document(answer)
                  
            markdown_content=f"""ðŸŒ‰ AI Travel Plan
    
            -----------------
    
            {answer}

            -----------------
    
            THIS TRAVEL PLAN WAS GENERATED BY AI. PLEASE VERIFY ALL INFORMATION.
         """
            
            st.markdown(markdown_content)


        else:
            st.error('Bot failed to respond'+ response.text)

    except Exception as e:
        st.error(f"The response failed due to {e}")


